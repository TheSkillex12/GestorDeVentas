    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tablaVentas.getRowCount() > 0) {
            int selectedRow = tablaVentas.getSelectedRow();
            if (selectedRow != -1) {
                int num_cliente = Integer.parseInt(String.valueOf(tablaVentas.getValueAt(selectedRow, 0)));
                JDialog jd = new JDialog(this, Dialog.ModalityType.APPLICATION_MODAL);
                EditarProducto ap = new EditarProducto(control.traerProducto(num_cliente));
                jd.setContentPane(ap.getContentPane());
                jd.pack();
                jd.setLocationRelativeTo(null);

                ap.getContentPane().addComponentListener(new ComponentAdapter() {
                    public void componentHidden(ComponentEvent e) {
                        System.out.println("Componente oculto");
                        actualizarInfo();
                        jd.dispose();
                    }
                });

                jd.setVisible(true);
            } else {
                mostrarMensaje("No seleccionó ningún producto", "Ventas - Kiosco", "error");
            }
        } else {
            mostrarMensaje("No hay nada para editar!", "Ventas - Kiosco", "error");
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        if (tablaVentas.getRowCount() > 0) {
            int selectedRow = tablaVentas.getSelectedRow();
            if (selectedRow != -1) {
                String producta = String.valueOf(tablaVentas.getValueAt(selectedRow, 2));
                int num_cliente = Integer.parseInt(String.valueOf(tablaVentas.getValueAt(selectedRow, 0)));
                mostrarMensaje("El producto: " + producta + " id: " + num_cliente + " ha sido borrado!", "Ventas - Kiosco", "info");
                control.borrarProducto(num_cliente);
                actualizarInfo();
            } else {
                mostrarMensaje("No seleccionó ningún producto", "Ventas - Kiosco", "error");
            }
        } else {
            mostrarMensaje("No hay nada para borrar!", "Ventas - Kiosco", "error");
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

