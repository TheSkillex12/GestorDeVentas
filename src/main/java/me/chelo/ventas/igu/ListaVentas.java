    private void actualizarInfo() {
        String vendeName = vendedor.getNombre()+" "+vendedor.getApellido().charAt(0)+".";
        
        txtVendedor.setText("VENDEDOR: "+ vendeName.toUpperCase());
        txtEntrada.setText("ENTRADA: "+vendedor.getEntrada());
        
        DefaultTableModel tabla = new DefaultTableModel(){
            
            @Override
            public boolean isCellEditable(int row, int column){
              return false;
            }
            
        };
        
        String titulos[] = {"Id","Cantidad","Producto", "Pago","Precio"};
        tabla.setColumnIdentifiers(titulos);
        
        List<Producto> listaProductos = control.traerProductos();
        
        if (listaProductos != null)
            listaProductos.forEach(p -> tabla.addRow(new Object[]{p.getId(), p.getCantidad(), p.getProducto(), p.getMetodoPago(), p.getPrecio()}));

        tablaVentas.setModel(tabla);
        tablaVentas.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tablaVentas.getColumnModel().getColumn(0).setPreferredWidth(35);
        tablaVentas.getColumnModel().getColumn(1).setPreferredWidth(70);
        tablaVentas.getColumnModel().getColumn(2).setPreferredWidth(235);
        tablaVentas.getColumnModel().getColumn(3).setPreferredWidth(58);
        tablaVentas.getColumnModel().getColumn(4).setPreferredWidth(93);
        txtCantidad.setText(tabla.getRowCount()+"");
        txtTotal.setText("$"+getTotal());
    }
    
    private Double getTotal(){
        return control.traerProductos()
                .stream()
                .mapToDouble(Producto::getPrecio)
                .sum();
    }
    
    private void mostrarMensaje(String msg, String titulo, String tipo){
        JOptionPane jp = new JOptionPane(msg);
        if(tipo.equalsIgnoreCase("info")){
            jp.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }else if(tipo.equalsIgnoreCase("error")){
            jp.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog d = jp.createDialog(titulo);
        d.setAlwaysOnTop(true);
        d.setVisible(true);
    }

